-- ONLINE FOOD MANAGEMENT SYSTEM SCHEMA (3NF)

-- DROP TABLES (in reverse order of dependencies)
DROP TABLE REVIEWS CASCADE CONSTRAINTS;
DROP TABLE DELIVERIES CASCADE CONSTRAINTS;
DROP TABLE PAYMENTS CASCADE CONSTRAINTS;
DROP TABLE ORDER_DETAILS CASCADE CONSTRAINTS;
DROP TABLE ORDERS CASCADE CONSTRAINTS;
DROP TABLE CARTS CASCADE CONSTRAINTS;
DROP TABLE MENU_ITEMS CASCADE CONSTRAINTS;
DROP TABLE MENU_CATEGORIES CASCADE CONSTRAINTS;
DROP TABLE RESTAURANTS CASCADE CONSTRAINTS;
DROP TABLE DELIVERY_AGENTS CASCADE CONSTRAINTS;
DROP TABLE CUSTOMERS CASCADE CONSTRAINTS;
DROP TABLE ADMINS CASCADE CONSTRAINTS;

-- 1. Customers Table
CREATE TABLE CUSTOMERS (
    customer_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    email VARCHAR2(100) UNIQUE NOT NULL,
    password VARCHAR2(100) NOT NULL,
    phone VARCHAR2(15) NOT NULL,
    address VARCHAR2(255) NOT NULL
);

-- 2. Restaurants Table
CREATE TABLE RESTAURANTS (
    restaurant_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    location VARCHAR2(255) NOT NULL,
    rating NUMBER(2,1) DEFAULT 0 CHECK (rating BETWEEN 0 AND 5)
);

-- 3. Menu Categories Table
CREATE TABLE MENU_CATEGORIES (
    category_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_name VARCHAR2(50) NOT NULL UNIQUE
);

-- 4. Menu Items Table
CREATE TABLE MENU_ITEMS (
    item_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    item_name VARCHAR2(100) NOT NULL,
    price NUMBER(10,2) NOT NULL CHECK (price >= 0),
    category_id NUMBER NOT NULL,
    restaurant_id NUMBER NOT NULL,
    CONSTRAINT fk_category FOREIGN KEY (category_id) REFERENCES MENU_CATEGORIES(category_id),
    CONSTRAINT fk_restaurant FOREIGN KEY (restaurant_id) REFERENCES RESTAURANTS(restaurant_id)
);

-- 5. Orders Table
CREATE TABLE ORDERS (
    order_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER NOT NULL,
    restaurant_id NUMBER NOT NULL,
    order_status VARCHAR2(20) DEFAULT 'PENDING' CHECK (order_status IN ('PENDING', 'CONFIRMED', 'OUT_FOR_DELIVERY', 'DELIVERED', 'CANCELLED')),
    total_amount NUMBER(10,2) DEFAULT 0,
    order_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_order_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(customer_id),
    CONSTRAINT fk_order_restaurant FOREIGN KEY (restaurant_id) REFERENCES RESTAURANTS(restaurant_id)
);

-- 6. Order Details Table (Junction Table for M:N between Orders and Menu Items)
CREATE TABLE ORDER_DETAILS (
    order_id NUMBER NOT NULL,
    item_id NUMBER NOT NULL,
    quantity NUMBER DEFAULT 1 CHECK (quantity > 0),
    unit_price NUMBER(10,2) NOT NULL,
    PRIMARY KEY (order_id, item_id),
    CONSTRAINT fk_detail_order FOREIGN KEY (order_id) REFERENCES ORDERS(order_id),
    CONSTRAINT fk_detail_item FOREIGN KEY (item_id) REFERENCES MENU_ITEMS(item_id)
);

-- 7. Payments Table
CREATE TABLE PAYMENTS (
    payment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id NUMBER NOT NULL UNIQUE,
    payment_method VARCHAR2(20) CHECK (payment_method IN ('CASH', 'CREDIT_CARD', 'UPI')),
    payment_status VARCHAR2(20) DEFAULT 'PENDING' CHECK (payment_status IN ('PENDING', 'COMPLETED', 'FAILED')),
    payment_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_payment_order FOREIGN KEY (order_id) REFERENCES ORDERS(order_id)
);

-- 8. Delivery Agents Table
CREATE TABLE DELIVERY_AGENTS (
    agent_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    agent_name VARCHAR2(100) NOT NULL,
    phone VARCHAR2(15) NOT NULL,
    is_available CHAR(1) DEFAULT 'Y' CHECK (is_available IN ('Y', 'N'))
);

-- 9. Deliveries Table
CREATE TABLE DELIVERIES (
    delivery_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id NUMBER NOT NULL UNIQUE,
    agent_id NUMBER,
    delivery_status VARCHAR2(20) DEFAULT 'ASSIGNED' CHECK (delivery_status IN ('ASSIGNED', 'OUT_FOR_DELIVERY', 'DELIVERED')),
    assignment_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    completion_time TIMESTAMP,
    CONSTRAINT fk_delivery_order FOREIGN KEY (order_id) REFERENCES ORDERS(order_id),
    CONSTRAINT fk_delivery_agent FOREIGN KEY (agent_id) REFERENCES DELIVERY_AGENTS(agent_id)
);

-- 10. Reviews Table
CREATE TABLE REVIEWS (
    review_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id NUMBER NOT NULL,
    customer_id NUMBER NOT NULL,
    rating NUMBER(1) CHECK (rating BETWEEN 1 AND 5),
    comments VARCHAR2(500),
    CONSTRAINT fk_review_order FOREIGN KEY (order_id) REFERENCES ORDERS(order_id),
    CONSTRAINT fk_review_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(customer_id)
);

-- 11. Cart Table (Temporary storage for user selection)
CREATE TABLE CARTS (
    customer_id NUMBER NOT NULL,
    item_id NUMBER NOT NULL,
    quantity NUMBER DEFAULT 1 CHECK (quantity > 0),
    PRIMARY KEY (customer_id, item_id),
    CONSTRAINT fk_cart_customer FOREIGN KEY (customer_id) REFERENCES CUSTOMERS(customer_id),
    CONSTRAINT fk_cart_item FOREIGN KEY (item_id) REFERENCES MENU_ITEMS(item_id)
);

-- 12. Admins Table
CREATE TABLE ADMINS (
    admin_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username VARCHAR2(50) UNIQUE NOT NULL,
    password VARCHAR2(100) NOT NULL
);
